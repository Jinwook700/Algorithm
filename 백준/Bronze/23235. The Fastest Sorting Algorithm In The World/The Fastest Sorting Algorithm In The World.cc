#include <iostream>
using namespace std;

int main() {
    int index = 1;
    
    while (true)
        {
            int N; cin >> N;
            if (N == 0) break;
            for (int i = 0; i < N; i++)
                {
                    int T; cin >> T;
                }
            cout << "Case " << index << ": Sorting... done!" << "\n";
            index++;
        }
    return 0;
}